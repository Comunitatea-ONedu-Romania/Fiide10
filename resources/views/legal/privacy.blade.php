<b>DRETURILE PERSOANEI VIZATE</b><br/>
1. • Dreptul de acces înseamnă dreptul persoanei vizate de a obține o confirmare din partea operatorului
că prelucrează sau nu datele cu caracter personal care il privesc și, în caz afirmativ, acces la datele
respective și la informații privind modalitatea în care sunt prelucrate datele.<br/>
2. • Dreptul la portabilitatea datelor se referă la dreptul de a primi datele personale într-un format
structurat, utilizat în mod curent și care poate fi citit automat și la dreptul că aceste date să fie transmise
direct altui operator, dacă acest lucru este fezabil din punct de vedere tehnic.<br/>
3. • Dreptul la opoziție&quot; vizează dreptul persoanei vizate de a se opune prelucrării datelor personale
atunci când prelucrarea este necesară pentru indeplinirea unei sarcini care servește unui interes public
sau când are în vedere un interes legitim al operatorului. Atunci când prelucrarea datelor cu caracter
personal are drept scop marketingul direct, persoana vizată are cu precadere dreptul de a se opune
prelucrării în orice moment.<br/>
4. • Dreptul la rectificare se referă la corectarea, fără întârzieri nejustificate, a datelor cu caracter personal
inexacte stocate. Rectificarea trebuie comunicată fiecarui destinatar la care au fost transmise datele, cu
excepția cazului în care acest lucru se dovedește imposibil sau presupune eforturi disproporționate
(demonstrabile).<br/>
5. • Dreptul la ștergerea datelor (&quot;dreptul de a fi uitat&quot;) înseamnă dreptul persoanei vizate de a solicita să
îi fie șterse datele cu caracter personal, fără întârzieri nejustificate, în cazul în care se aplică unul dintre
următoarele motive: acestea nu mai sunt necesare pentru îndeplinirea scopurilor pentru care au fost
colectate sau prelucrate; își retrage consimțământul și nu există niciun alt temei juridic pentru
prelucrare; se opune prelucrării și nu există motive legitime care să prevaleze; datele cu caracter
personal au fost prelucrate ilegal; datele cu caracter personal trebuie șterse pentru respectarea unei
obligații legale; datele cu caracter personal au fost colectate în legătură cu oferirea de servicii ale
societății informaționale.<br/>
6. • Dreptul la restricționarea prelucrării  poate fi exercitat în cazul în care persoana contestă exactitatea
datelor, pe o perioadă care permite verificarea corectitudinii datelor; prelucrarea este ilegală, iar
persoana se opune ștergerii datelor cu caracter personal, solicitând în schimb restricționarea; în cazul în
care operatorul nu mai are nevoie de datele cu caracter personal în scopul prelucrării, dar persoana i le
solicită pentru constatarea, exercitarea sau apărarea unui drept în instanță; în cazul în care persoana s-a
opus prelucrării pentru intervalul de timp în care se verifică dacă drepturile legitime ale operatorului
prevalează asupra celor ale persoanei respective.</p>
